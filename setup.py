def install_backend_deps():
    """Install Python dependencies."""
    print_header("Installing Backend Dependencies")

    backend_dir = Path(__file__).parent / "backend"
    requirements_file = backend_dir / "requirements.txt"

    if not requirements_file.exists():
        print_error("requirements.txt not found")
        return False

    print_info("Installing Python packages (this may take a few minutes)...")

    # Create virtual environment if it doesn't exist
    venv_dir = backend_dir / "venv"
    if not venv_dir.exists():
        print_info("Creating virtual environment...")
        success, _ = run_command(f"{sys.executable} -m venv venv", cwd=backend_dir)
        if not success:
            print_warning("Could not create venv, installing to system Python")

    # Install requirements
    if venv_dir.exists():
        if sys.platform == "win32":
            pip_path = venv_dir / "Scripts" / "pip"
        else:
            pip_path = venv_dir / "bin" / "pip"
        install_cmd = f"{pip_path} install -r requirements.txt"
    else:
        install_cmd = f"{sys.executable} -m pip install -r requirements.txt"

    success, _ = run_command(install_cmd, cwd=backend_dir)

    if success:
        print_success("Backend dependencies installed")
        return True
    else:
        print_error("Failed to install backend dependencies")
        print_info("  Try manually: cd backend && pip install -r requirements.txt")
        return False


def install_frontend_deps():
    """Install Node.js dependencies."""
    print_header("Installing Frontend Dependencies")

    frontend_dir = Path(__file__).parent / "frontend"
    package_json = frontend_dir / "package.json"

    if not package_json.exists():
        print_warning("package.json not found, skipping frontend setup")
        return True

    if not check_command("npm"):
        print_warning("npm not found, skipping frontend setup")
        print_info("  Install Node.js from https://nodejs.org/")
        return True

    print_info("Installing npm packages...")
    success, _ = run_command("npm install", cwd=frontend_dir)

    if success:
        print_success("Frontend dependencies installed")
        return True
    else:
        print_error("Failed to install frontend dependencies")
        print_info("  Try manually: cd frontend && npm install")
        return False


def create_directories():
    """Create necessary directories."""
    print_header("Creating Directories")

    project_root = Path(__file__).parent

    directories = [
        project_root / "backend" / "ml" / "saved_models",
        project_root / "backend" / "data" / "historical",
        project_root / "backend" / "logs",
    ]

    for dir_path in directories:
        dir_path.mkdir(parents=True, exist_ok=True)
        print_success(f"Created {dir_path.relative_to(project_root)}")

    return True


