#!/usr/bin/env python3
"""
Sports Prediction Platform - Setup Script

This script automates the initial setup process:
1. Creates .env file from template
2. Installs Python dependencies
3. Installs Node.js dependencies
4. Checks for Docker
5. Provides next steps

Usage:
    python setup.py           # Interactive setup
    python setup.py --quick   # Skip prompts, use defaults
"""

import os
import sys
import subprocess
import shutil
from pathlib import Path


# Colors for terminal output
class Colors:
    GREEN = '\033[92m'
    YELLOW = '\033[93m'
    RED = '\033[91m'
    BLUE = '\033[94m'
    BOLD = '\033[1m'
    END = '\033[0m'


def print_header(text):
    print(f"\n{Colors.BOLD}{Colors.BLUE}{'='*60}{Colors.END}")
    print(f"{Colors.BOLD}{Colors.BLUE}{text}{Colors.END}")
    print(f"{Colors.BOLD}{Colors.BLUE}{'='*60}{Colors.END}\n")


def print_success(text):
    print(f"{Colors.GREEN}✓ {text}{Colors.END}")


def print_warning(text):
    print(f"{Colors.YELLOW}⚠ {text}{Colors.END}")


def print_error(text):
    print(f"{Colors.RED}✗ {text}{Colors.END}")


def print_info(text):
    print(f"{Colors.BLUE}→ {text}{Colors.END}")


def check_command(cmd):
    """Check if a command exists."""
    return shutil.which(cmd) is not None


def run_command(cmd, cwd=None, capture=False):
    """Run a shell command."""
    try:
        if capture:
            result = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
            return result.returncode == 0, result.stdout
        else:
            result = subprocess.run(cmd, shell=True, cwd=cwd)
            return result.returncode == 0, ""
    except Exception as e:
        return False, str(e)


def check_prerequisites():
    """Check that all prerequisites are installed."""
    print_header("Checking Prerequisites")

    all_good = True

    # Python
    python_version = sys.version_info
    if python_version >= (3, 11):
        print_success(f"Python {python_version.major}.{python_version.minor} (3.11+ required)")
    elif python_version >= (3, 9):
        print_warning(f"Python {python_version.major}.{python_version.minor} (3.11+ recommended, 3.9+ minimum)")
    else:
        print_error(f"Python {python_version.major}.{python_version.minor} (3.9+ required)")
        all_good = False

    # Docker
    if check_command("docker"):
        success, version = run_command("docker --version", capture=True)
        if success:
            print_success(f"Docker installed: {version.strip()}")
        else:
            print_success("Docker installed")
    else:
        print_error("Docker not found - install from https://docs.docker.com/get-docker/")
        all_good = False

    # Docker Compose
    if check_command("docker-compose") or check_command("docker"):
        success, _ = run_command("docker compose version", capture=True)
        if success:
            print_success("Docker Compose available")
        else:
            success, _ = run_command("docker-compose --version", capture=True)
            if success:
                print_success("Docker Compose available (legacy)")
            else:
                print_error("Docker Compose not available")
                all_good = False

    # Node.js
    if check_command("node"):
        success, version = run_command("node --version", capture=True)
        if success:
            print_success(f"Node.js installed: {version.strip()}")
    else:
        print_warning("Node.js not found - needed for frontend development")
        print_info("  Install from https://nodejs.org/ (18+ recommended)")

    # npm
    if check_command("npm"):
        print_success("npm available")
    else:
        print_warning("npm not found")

    return all_good


