def main():
    """Main setup flow."""
    print(f"""
{Colors.BOLD}{Colors.BLUE}
╔═══════════════════════════════════════════════════════════╗
║         Sports Prediction Platform - Setup                ║
╚═══════════════════════════════════════════════════════════╝
{Colors.END}""")

    quick_mode = "--quick" in sys.argv

    # Check prerequisites
    if not check_prerequisites():
        print_error("\nSome prerequisites are missing. Please install them and re-run setup.")
        sys.exit(1)

    # Setup .env
    setup_env_file()

    # Create directories
    create_directories()

    # Ask about installing dependencies
    if not quick_mode:
        print("\n")
        response = input("Install Python backend dependencies? (Y/n): ").strip().lower()
        if response != 'n':
            install_backend_deps()

        response = input("Install Node.js frontend dependencies? (Y/n): ").strip().lower()
        if response != 'n':
            install_frontend_deps()
    else:
        install_backend_deps()
        install_frontend_deps()

    # Print next steps
    print_next_steps()


if __name__ == "__main__":
    main()
