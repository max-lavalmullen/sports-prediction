"""
Train Player Prop Models.

Loads historical player stats, generates features, and trains XGBoost regressors
for Points, Rebounds, and Assists.
"""
import asyncio
import sys
import os
import pandas as pd
from sqlalchemy import select
from loguru import logger

# Add backend to path
sys.path.append(os.path.join(os.path.dirname(__file__), '../../..'))

from app.core.database import AsyncSessionLocal
from app.models.game import PlayerGameStats, Game
from ml.features.nba_player_features import feature_engineer
from ml.models.prop_model import PlayerPropModel

async def load_training_data(limit: int = 50000):
    """Load player stats joined with game dates."""
    logger.info("Loading training data from database...")
    
    async with AsyncSessionLocal() as session:
        # Join Stats with Game to get the date
        query = (
            select(PlayerGameStats, Game.scheduled_time)
            .join(Game, PlayerGameStats.game_id == Game.id)
            .limit(limit)
        )
        
        result = await session.execute(query)
        rows = result.all()
        
        data = []
        for stat, game_date in rows:
            # Flatten structure
            # stat.stats is the JSON content (e.g. {"PTS": 25, "REB": 5 ...})
            # Note: keys might be uppercase from NBA API
            row = stat.stats.copy()
            
            # Normalize keys to lowercase for feature engineer
            row = {k.lower(): v for k, v in row.items()}
            
            row['player_id'] = stat.player_id
            row['game_id'] = stat.game_id
            row['date'] = game_date
            
            # Ensure we have numeric targets
            # Map API keys to target keys if needed (e.g., 'pts' vs 'PTS')
            # NBA API usually returns 'PTS', 'REB', 'AST'
            
            data.append(row)
            
    logger.info(f"Loaded {len(data)} rows.")
    return data

async def train_all_props():
    # 1. Load Data
    raw_data = await load_training_data()
    if not raw_data:
        logger.error("No data found. Run backfill script first.")
        return

    # 2. Feature Engineering
    logger.info("Generating features...")
    df = feature_engineer.process_player_logs(raw_data)
    
    if df.empty:
        logger.error("Feature engineering returned empty DataFrame.")
        return

    # Drop rows where targets are NaN (shouldn't happen with valid logs)
    df = df.dropna(subset=['pts', 'reb', 'ast'])
    
    # Define features to use
    # We use the rolling averages generated by the engineer
    feature_cols = [c for c in df.columns if any(x in c for x in ['_5g', '_10g', '_20g', '_ewma', 'days_rest'])]
    logger.info(f"Using {len(feature_cols)} features: {feature_cols[:5]}...")

    # 3. Train Models
    targets = ['pts', 'reb', 'ast']
    
    results = {}
    for target in targets:
        logger.info(f"\n--- Training {target.upper()} Model ---")
        model = PlayerPropModel(stat_type=target)
        
        # Train
        metrics = model.train(df, feature_cols, target)
        results[target] = metrics
        
        # Save
        model.save()

    logger.info("\nTraining Complete!")
    logger.info(f"Results: {results}")

if __name__ == "__main__":
    try:
        asyncio.run(train_all_props())
    except KeyboardInterrupt:
        print("\nStopped.")
    except Exception as e:
        logger.error(f"Fatal error: {e}")
